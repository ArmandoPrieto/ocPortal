/**
 * Module page class.
 */
class Module_edittitle
{

	/**
	 * Standard modular info function.
	 *
	 * @return ?array	Map of module info (NULL: module is disabled).
	 */
	function info()
	{
		$info=array();
		$info['author']='Chris Graham';
		$info['organisation']='ocProducts';
		$info['hacked_by']=NULL;
		$info['hack_version']=NULL;
		$info['version']=2;
		$info['locked']=false;
		return $info;
	}
	
	/**
	 * Standard modular entry-point finder function.
	 *
	 * @return ?array	A map of entry points (type-code=>language-code) (NULL: disabled).
	 */
	function get_entry_points()
	{
		return array('misc'=>'EDIT_TITLE');
	}
	
	/**
	 * Standard modular run function.
	 *
	 * @return tempcode	The result of execution.
	 */
	function run()
	{
		if (get_forum_type()!='ocf') warn_exit(do_lang_tempcode('NO_OCF')); else ocf_require_all_forum_stuff();
		require_css('ocf');

		$member_id=get_param_integer('id',get_member());
		enforce_personal_access($member_id,NULL,'member_maintenance');

		if ($GLOBALS['FORUM_DRIVER']->get_member_row_field($member_id,'m_password_compat_scheme')=='remote')
		{
			warn_exit(do_lang_tempcode('DISALLOWED_REMOTE_MEMBER_ACTION'));
		}

		check_specific_permission('may_choose_custom_title');
	
		$type=get_param('type','misc');
		if ($type=='misc') return $this->gui();
		if ($type=='actual') return $this->actual();
	
		return new ocp_tempcode();
	}
	
	/**
	 * The UI for editing a members title.
	 *
	 * @return tempcode	The UI.
	 */
	function gui()
	{
		$title=get_page_title('EDIT_TITLE');
	
		$member_id=get_param_integer('id',get_member());
	
		// UI fields
		$fields=new ocp_tempcode();
		$_title=$GLOBALS['FORUM_DRIVER']->get_member_row_field($member_id,'m_title');
		require_code('form_templates');
		$fields->attach(form_input_line(do_lang_tempcode('TITLE'),do_lang_tempcode('DESCRIPTION_MEMBER_TITLE',escape_html($GLOBALS['FORUM_DRIVER']->get_username($member_id))),'title',$_title,false,NULL,intval(get_option('max_member_title_length'))));
	
		$submit_name=do_lang_tempcode('SAVE');
	
		$post_url=build_url(array('page'=>'_SELF','type'=>'actual','id'=>$member_id),'_SELF');
	
		$text='';
	
		return do_template('FORM_SCREEN',array('_GUID'=>'cb867c164bee2e492ccd416d8ec93ef6','HIDDEN'=>'','TITLE'=>$title,'TEXT'=>$text,'FIELDS'=>$fields,'SUBMIT_NAME'=>$submit_name,'URL'=>$post_url));
	}
	
	/**
	 * The actualiser for editing a members title.
	 *
	 * @return tempcode	The UI.
	 */
	function actual()
	{
		$title=get_page_title('EDIT_TITLE');
	
		$member_id=get_param_integer('id',get_member());
	
		$_title=post_param('title');
		require_code('ocf_members_action');
		require_code('ocf_members_action2');
		ocf_member_choose_title($_title,$member_id);
	
		breadcrumb_set_parents(array(array('_SELF:_SELF:misc:'.strval($member_id),do_lang_tempcode('EDIT_TITLE'))));
		breadcrumb_set_self(do_lang_tempcode('DONE'));

		// Send back to start
		$url=build_url(array('page'=>'_SELF','id'=>$member_id),'_SELF');
		return redirect_screen($title,$url,do_lang_tempcode('SUCCESS'));
	}

}


