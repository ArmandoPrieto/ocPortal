/**
 * Module page class.
 */
class Module_editsignature
{

	/**
	 * Standard modular info function.
	 *
	 * @return ?array	Map of module info (NULL: module is disabled).
	 */
	function info()
	{
		$info=array();
		$info['author']='Chris Graham';
		$info['organisation']='ocProducts';
		$info['hacked_by']=NULL;
		$info['hack_version']=NULL;
		$info['version']=2;
		$info['locked']=false;
		return $info;
	}
	
	/**
	 * Standard modular entry-point finder function.
	 *
	 * @return ?array	A map of entry points (type-code=>language-code) (NULL: disabled).
	 */
	function get_entry_points()
	{
		return array('misc'=>'EDIT_SIGNATURE');
	}
	
	/**
	 * Standard modular run function.
	 *
	 * @return tempcode	The result of execution.
	 */
	function run()
	{
		if (get_forum_type()!='ocf') warn_exit(do_lang_tempcode('NO_OCF')); else ocf_require_all_forum_stuff();
		require_css('ocf');
	
		$member_id=get_param_integer('id',get_member());
		enforce_personal_access($member_id,NULL,'member_maintenance');
	
		if ($GLOBALS['FORUM_DRIVER']->get_member_row_field($member_id,'m_password_compat_scheme')=='remote')
		{
			warn_exit(do_lang_tempcode('DISALLOWED_REMOTE_MEMBER_ACTION'));
		}

		$type=get_param('type','misc');
		if ($type=='misc') return $this->gui();
		if ($type=='actual') return $this->actual();

		return new ocp_tempcode();
	}
	
	/**
	 * The UI for editing a members signature.
	 *
	 * @return tempcode	The UI.
	 */
	function gui()
	{
		$title=get_page_title('EDIT_SIGNATURE');
	
		$member_id=get_param_integer('id',get_member());
	
		$_signature=get_translated_tempcode($GLOBALS['FORUM_DRIVER']->get_member_row_field($member_id,'m_signature'),$GLOBALS['FORUM_DB']);
		$signature=($_signature->is_empty())?do_lang_tempcode('NONE_EM'):$_signature;
		$_signature_original=get_translated_text($GLOBALS['FORUM_DRIVER']->get_member_row_field($member_id,'m_signature'),$GLOBALS['FORUM_DB']);

		$post_url=build_url(array('page'=>'_SELF','type'=>/*'preview'*/'actual','id'=>$member_id),'_SELF');

		$size=ocf_get_member_best_group_property($member_id,'max_sig_length_comcode');
		
		$javascript="
			var form=document.getElementById('post').form;
			form.old_submit=form.onsubmit;
			form.onsubmit=function()
				{
					var post=form.elements['post'];
					if ((!post.value) && (post[1])) post=post[1];
					if (post.value.length>".strval($size).")
					{
						window.alert('".php_addslashes(do_lang('SIGNATURE_TOO_BIG'))."');
						return false;
					}
					if (typeof form.old_submit!='undefined' && form.old_submit) return form.old_submit();
					return true;
				};
		";
	
		require_code('form_templates');
		$posting_form=get_posting_form(do_lang('SAVE'),$_signature_original,$post_url,new ocp_tempcode(),new ocp_tempcode(),do_lang('SIGNATURE'),'',NULL,$_signature,$javascript);

		return do_template('OCF_EDIT_SIGNATURE_SCREEN',array('_GUID'=>'f5f2eb2552c34840c9cf46886422401e','SIZE'=>integer_format($size),'SIGNATURE'=>$signature,'TITLE'=>$title,'POSTING_FORM'=>$posting_form));
	}
	
	/**
	 * The actualiser for editing a members signature.
	 *
	 * @return tempcode	The UI.
	 */
	function actual()
	{
		$title=get_page_title('EDIT_SIGNATURE');
	
		$member_id=get_param_integer('id',get_member());
	
		$signature=post_param('post');
	
		require_code('ocf_members_action');
		require_code('ocf_members_action2');
		ocf_member_choose_signature($signature,$member_id);
	
		breadcrumb_set_parents(array(array('_SELF:_SELF:misc:'.strval($member_id),do_lang_tempcode('EDIT_SIGNATURE'))));
		breadcrumb_set_self(do_lang_tempcode('DONE'));

		require_code('autosave');
		clear_ocp_autosave();

		// Send back to start
		$url=build_url(array('page'=>'_SELF','type'=>'misc','id'=>$member_id),'_SELF');
		return redirect_screen($title,$url,do_lang_tempcode('SUCCESS'));
	}

}


