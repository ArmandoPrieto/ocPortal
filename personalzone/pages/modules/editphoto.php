/**
 * Module page class.
 */
class Module_editphoto
{

	/**
	 * Standard modular info function.
	 *
	 * @return ?array	Map of module info (NULL: module is disabled).
	 */
	function info()
	{
		$info=array();
		$info['author']='Chris Graham';
		$info['organisation']='ocProducts';
		$info['hacked_by']=NULL;
		$info['hack_version']=NULL;
		$info['version']=2;
		$info['locked']=false;
		return $info;
	}
	
	/**
	 * Standard modular entry-point finder function.
	 *
	 * @return ?array	A map of entry points (type-code=>language-code) (NULL: disabled).
	 */
	function get_entry_points()
	{
		return array('misc'=>'EDIT_PHOTO');
	}
	
	/**
	 * Standard modular run function.
	 *
	 * @return tempcode	The result of execution.
	 */
	function run()
	{
		if (get_forum_type()!='ocf') warn_exit(do_lang_tempcode('NO_OCF')); else ocf_require_all_forum_stuff();
	
		$member_id=get_param_integer('id',get_member());
		enforce_personal_access($member_id,NULL,'member_maintenance');
	
		if ($GLOBALS['FORUM_DRIVER']->get_member_row_field($member_id,'m_password_compat_scheme')=='remote')
		{
			warn_exit(do_lang_tempcode('DISALLOWED_REMOTE_MEMBER_ACTION'));
		}

		$type=get_param('type','misc');
		if ($type=='misc') return $this->gui();
		if ($type=='actual') return $this->actual();
	
		return new ocp_tempcode();
	}
	
	/**
	 * The UI for editing a members photo.
	 *
	 * @return tempcode	The UI.
	 */
	function gui()
	{
		$title=get_page_title('EDIT_PHOTO');
	
		/*if (get_base_url()!=get_forum_base_url())
		{
			require_code('site2');
			assign_refresh(str_replace(get_base_url(),get_forum_base_url(),get_self_url(true)));
			return do_template('REDIRECT_SCREEN',array('_GUID'=>'24b4cda0b0be38a412160fc3964ed5dd','URL'=>$url,'TITLE'=>$title,'TEXT'=>do_lang_tempcode('REDIRECTING')));
		}*/

		$member_id=get_param_integer('id',get_member());
	
		$photo_url=$GLOBALS['FORUM_DRIVER']->get_member_row_field($member_id,'m_photo_url');
		$thumb_url=$GLOBALS['FORUM_DRIVER']->get_member_row_field($member_id,'m_photo_thumb_url');
	
		// UI fields
		$fields=new ocp_tempcode();
		require_code('form_templates');
		$fields->attach(form_input_upload(do_lang_tempcode('UPLOAD'),do_lang_tempcode('DESCRIPTION_UPLOAD'),'file',false,NULL,NULL,true,str_replace(' ','',get_option('valid_images'))));
		$fields->attach(form_input_line(do_lang_tempcode('ALT_FIELD',do_lang_tempcode('URL')),do_lang_tempcode('DESCRIPTION_ALTERNATE_URL'),'url',$photo_url,false));
		if (get_option('is_on_gd')=='0')
		{
			$thumb_width=get_option('thumb_width');
			$fields->attach(form_input_upload(do_lang_tempcode('THUMBNAIL'),do_lang_tempcode('DESCRIPTION_THUMBNAIL',escape_html($thumb_width)),'file2',false,NULL,NULL,true,str_replace(' ','',get_option('valid_images'))));
			$fields->attach(form_input_line(do_lang_tempcode('ALT_FIELD',do_lang_tempcode('URL')),do_lang_tempcode('DESCRIPTION_ALTERNATE_URL'),'thumb_url',$thumb_url,false));
		}
		$hidden=new ocp_tempcode();
		handle_max_file_size($hidden,'image');

		$submit_name=do_lang_tempcode('SAVE');
	
		$post_url=build_url(array('page'=>'_SELF','type'=>'actual','id'=>$member_id,'uploading'=>1),'_SELF');
	
		$text=new ocp_tempcode();
		require_code('images');
		$max=floatval(get_max_image_size())/floatval(1024*1024);
		if ($max<3.0)
		{
			require_code('files2');
			$config_url=get_upload_limit_config_url();
			$text->attach(paragraph(do_lang_tempcode(is_null($config_url)?'MAXIMUM_UPLOAD':'MAXIMUM_UPLOAD_STAFF',escape_html(($max>10.0)?integer_format(intval($max)):float_format($max)),escape_html($config_url))));
		}

		return do_template('FORM_SCREEN',array('_GUID'=>'1f7e7d8e82dafd035aadca42a796c4f2','HIDDEN'=>$hidden,'TITLE'=>$title,'TEXT'=>$text,'FIELDS'=>$fields,'SUBMIT_NAME'=>$submit_name,'URL'=>$post_url,'JAVASCRIPT'=>'standardAlternateFields(\'file\',\'url\'); standardAlternateFields(\'file2\',\'thumb_url\');'));
	}

	/**
	 * The actualiser for editing a members photo.
	 *
	 * @return tempcode	The UI.
	 */
	function actual()
	{
		$title=get_page_title('EDIT_PHOTO');

		$member_id=get_param_integer('id',get_member());
	
		breadcrumb_set_parents(array(array('_SELF:_SELF:misc:'.strval($member_id),do_lang_tempcode('EDIT_PHOTO'))));

		require_code('ocf_members_action');
		require_code('ocf_members_action2');
		ocf_member_choose_photo('url','file',$member_id);
	
		breadcrumb_set_self(do_lang_tempcode('DONE'));

		// Send back to start
		$url=$GLOBALS['FORUM_DRIVER']->member_profile_link($member_id,false,true);
		return redirect_screen($title,$url,do_lang_tempcode('SUCCESS'));
	}

}


