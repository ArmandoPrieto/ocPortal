			$this->handle_resizing_and_watermarking();
		}
		$validated=post_param_integer('validated',fractional_edit()?INTEGER_MAGIC_NULL:0);
		$urls=get_url('url','file','uploads/galleries',0,OCP_UPLOAD_IMAGE,true,'thumb_url','file2');
