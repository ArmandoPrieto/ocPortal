		$description=post_param('description',STRING_MAGIC_NULL);

		$cat=post_param('cat',STRING_MAGIC_NULL);
		if ($cat!=STRING_MAGIC_NULL)
		{
			if (can_submit_to_gallery($cat)===false) access_denied('SUBMIT_HERE');
