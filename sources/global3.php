 */
function ocp_mb_strtolower($in)
{
	if (strtolower(get_charset())!='utf-8') return strtolower($in);

	if (function_exists('mb_strtolower')) return @mb_strtolower($in,get_charset());

	return is_ascii_string($in)?strtolower($in):$in;
}

/**
