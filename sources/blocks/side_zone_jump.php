class Block_side_zone_jump
{

	/**
	 * Standard modular info function.
	 *
	 * @return ?array	Map of module info (NULL: module is disabled).
	 */
	function info()
	{
		$info=array();
		$info['author']='Chris Graham';
		$info['organisation']='ocProducts';
		$info['hacked_by']=NULL;
		$info['hack_version']=NULL;
		$info['version']=2;
		$info['locked']=false;
		$info['parameters']=array('table','skip','reorder');
		return $info;
	}
	
	/**
	 * Standard modular cache function.
	 *
	 * @return ?array	Map of cache details (cache_on and ttl) (NULL: module is disabled).
	 */
	function cacheing_environment()
	{
		$info=array();
		$info['cache_on']='array(get_zone_name(),$GLOBALS[\'FORUM_DRIVER\']->get_members_groups(get_member(),false,true),array_key_exists(\'table\',$map)?$map[\'table\']:\'0\',array_key_exists(\'skip\',$map)?$map[\'skip\']:\'-1\',array_key_exists(\'reorder\',$map)?$map[\'reorder\']:\'-1\')';
		$info['ttl']=15;
		return $info;
	}
	
	/**
	 * Standard modular run function.
	 *
	 * @param  array		A map of parameters.
	 * @return tempcode	The result of execution.
	 */
	function run($map)
	{
		require_css('side_blocks');
		
		require_code('zones3');
		
		$skip=array_key_exists('skip',$map)?$map['skip']:'-1';
		$reorder=array_key_exists('reorder',$map)?$map['reorder']:'-1';

		$content=get_zone_chooser(false,($skip=='-1')?NULL:explode(',',$skip),($reorder=='-1')?NULL:explode(',',$reorder));
	
		if ((array_key_exists('table',$map)) && ($map['table']=='1')) return do_template('BLOCK_SIDE_ZONE_JUMP',array('_GUID'=>'7d99038e936b8be1f8d829d71ff40d5a','CONTENT'=>$content));
	
		return $content;
	}

}


