class Hook_main_custom_gfx_sifr
{

	/**
	 * Standard modular run function. Creates custom graphics from parameters.
	 *
	 * @param  array		Map of hook parameters (relayed from block parameters map).
	 * @param  object		The block itself (contains utility methods).
	 * @return tempcode	HTML to output.
	 */
	function run($map,&$Block)
	{
		require_css('sifr');
		require_javascript('javascript_sifr');

		$tag=array_key_exists('tag',$map)?$map['tag']:'h2';

		if (!array_key_exists('data',$map)) $map['data']=do_lang('FILL_IN_DATA_PARAM');

		$id='sifr'.uniqid('');

		$font=array_key_exists('font',$map)?$map['font']:'tradegothic';
		if (substr($font,-4)=='.swf') $font=substr($font,0,strlen($font)-4);
		if (file_exists(get_file_base().'/data_custom/sifr/'.$font.'.swf'))
		{
			$font=get_base_url().'/data_custom/sifr/'.$font.'.swf';
		} else
		{
			$font=get_base_url().'/data/sifr/'.$font.'.swf';
		}

		$extra='';
		if (!array_key_exists('color',$map)) $map['color']='#000000';
		$extra.=', sColor:"'.php_addslashes($map['color']).'"';
		if (array_key_exists('link_color',$map)) $extra.=', sBgColor:"'.php_addslashes($map['link_color']).'"';
		if (array_key_exists('hover_color',$map)) $extra.=', sHoverColor:"'.php_addslashes($map['hover_color']).'"';
		if (array_key_exists('padding_top',$map)) $extra.=', nPaddingTop:"'.php_addslashes($map['padding_top']).'"';
		if (array_key_exists('padding_bottom',$map)) $extra.=', nPaddingBottom:"'.php_addslashes($map['padding_bottom']).'"';
		if (array_key_exists('padding_left',$map)) $extra.=', nPaddingLeft:"'.php_addslashes($map['padding_left']).'"';
		if (array_key_exists('padding_right',$map)) $extra.=', nPaddingRight:"'.php_addslashes($map['padding_right']).'"';
		if (array_key_exists('flash_vars',$map)) $extra.=', sFlashVars:"'.php_addslashes($map['flash_vars']).'"';
		if (array_key_exists('case',$map)) $extra.=', sCase:"'.php_addslashes($map['case']).'"';
		if (!array_key_exists('wmode',$map)) $map['wmode']='transparent';
		$extra.=', sWmode:"'.php_addslashes($map['wmode']).'"';

		$ret='<'.$tag.' id="'.escape_html($id).'">'.$map['data'].'</'.$tag.'>';
		$ret.='
			<script type="text/javascript">//<![CDATA[
			if (typeof sIFR=="function")
				sIFR.replaceElement(named({sSelector:"#'.php_addslashes($id).'", sFlashSrc:"'.php_addslashes($font).'"'.$extra.'}));
			//]]></script>';

		if (function_exists('ocp_mark_as_escaped')) ocp_mark_as_escaped($ret);
		return make_string_tempcode($ret);
	}

}


